"""Лёгкий пробник геометрии медиафайла для триггера адаптивной ориентации.

ИНВАРИАНТ ПОТОКА: этот модуль БЕЗОПАСЕН для вызова из GUI-потока. Растровая
проба читает ТОЛЬКО заголовок через QImageReader (без декода пикселей), а для
ВИДЕО проба здесь намеренно НЕ выполняется — `probe_media_ar` возвращает None,
сигналя вызывающей стороне перейти на реактивную модель (см. ниже). Раньше
видеоветка дёргала синхронный `cv2.VideoCapture(path)` + `cap.read()`: на
гигабайтных длинных MP4 парс заголовка/MOOV-атома занимал секунды/минуты и
намертво вешал главный поток Qt (GUI Thread Lockup). Эта ветка ликвидирована.

КОНТУРЫ РАСЧЁТА AR:
- растры (JPG/PNG/WEBP/BMP/GIF): только заголовок через QImageReader, без
  декода пикселей; EXIF-поворот учитывается (телефонные портреты, у которых
  матрица хранит ландшафт + флаг поворота, не классифицируются ошибочно);
- видео: AR вычисляется РЕАКТИВНО из асинхронного размера кадра — нативный
  сигнал QVideoSink.videoSizeChanged(QSize) или размер cv2-превью, поднятого
  фоновым воркером. `probe_media_ar` для видео возвращает None.

Любой сбой пробы → None (вызывающая сторона сохраняет классическую компоновку).
"""

from pathlib import Path

# Расширения, для которых синхронная проба ЗАПРЕЩЕНА: их AR приходит реактивно.
VIDEO_EXTS = {'.mp4', '.mov', '.mkv', '.webm', '.avi', '.m4v'}

# Порог оси раскладки пары: AR >= порога → TopToBottom (широкие кадры берут
# полную ширину строки), ниже → LeftToRight (кадры делят ширину бок-о-бок).
# Значение 1.5 (а НЕ интуитивные 1.0) намеренно: при пороге 1.0 формат 4:3
# (AR ~1.33) уходил в вертикальный стек и упирался в пиллар-боксинг — широкие
# пустые поля по бокам каждого кадра. 1.5 ≈ середина между 4:3 (1.33) и 16:9
# (1.78), поэтому 4:3 остаётся в горизонтальном сплите (эффективнее по площади),
# а истинно широкие кадры (>=16:9) по-прежнему стекаются вертикально.
LANDSCAPE_AR_THRESHOLD = 1.5


def probe_media_ar(path):
    """AR = Width / Height медиафайла; None, если проба не удалась ИЛИ это видео.

    Для видео всегда возвращает None — синхронный cv2-парс в GUI-потоке устранён;
    ориентацию видео задаёт реактивный слот по асинхронному размеру кадра.
    Растровая проба читает лишь заголовок (QImageReader) и безопасна в UI-потоке.
    """
    try:
        if Path(path).suffix.lower() in VIDEO_EXTS:
            return None
        return _raster_ar(path)
    except Exception:
        return None


def _raster_ar(path):
    from PySide6.QtGui import QImageReader
    reader = QImageReader(str(path))
    size = reader.size()
    if not size.isValid() or size.height() <= 0:
        return None
    w, h = size.width(), size.height()
    # size() отдаёт геометрию ДО EXIF-поворота; значения Transformation >= 4
    # (Rotate90/270 и их зеркальные варианты) означают транспонирование осей.
    if reader.transformation().value >= 4:
        w, h = h, w
    return w / h


def direction_for_ar(ar):
    """AR → ось QBoxLayout для пары карточек предпросмотра/сравнения.

    Единая точка политики ориентации (использует и синхронная растровая проба,
    и реактивный видео-слот): AR < LANDSCAPE_AR_THRESHOLD (портрет, квадрат и
    умеренно-широкие до 4:3) ЛИБО None (проба не удалась/ещё не готова) →
    LeftToRight — кадры делят ширину бок-о-бок. AR >= порога (истинно широкие,
    >=16:9) → TopToBottom — широкие кадры берут полную ширину строки вместо
    урезанной половины. Порог = 1.5 (см. LANDSCAPE_AR_THRESHOLD: 4:3 ~1.33
    остаётся в горизонтальном сплите, не упираясь в пиллар-боксинг). Возврат —
    готовое значение QBoxLayout.Direction.
    """
    from PySide6.QtWidgets import QBoxLayout
    if ar is not None and ar >= LANDSCAPE_AR_THRESHOLD:
        return QBoxLayout.Direction.TopToBottom
    return QBoxLayout.Direction.LeftToRight
